package pratica.exercicioconstrutores;

public class Conta {
 private int numeroconta;
 private String nome;
 private int conta;

    public int getNumeroconta() {
        return numeroconta;
    }



    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getConta() {
        return conta;
    }

    public void setConta(int conta) {
        this.conta = conta;
    }
}
